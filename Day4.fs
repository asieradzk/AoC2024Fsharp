let width = allLinesForward.[4].Length
let height = allLinesForward.Length
let allLinesDownardChars = [0..width-1] |> List.map ( fun column -> [0..height-1] |> List.map (fun row -> allLinesForward.[row].[column]))
let allLinesDownward = allLinesDownardChars |> List.map (fun x -> new String(x |> List.toArray))
let allLinesUpward = allLinesDownward |> List.map (fun x -> new String(x.ToCharArray() |> Array.rev))

type coordinate = {x:int; y:int}
let allTopLeftCoordinates = 
    let leftEdge = [0..height-1] |> List.map (fun y -> {x=0; y=y})        // Down the left side
    let topEdge = [1..width-1] |> List.map (fun x -> {x=x; y=0})            // Across the top (skip 0,0)
    leftEdge @ topEdge

let allTopRightCoordinates = 
    let rightEdge = [0..height-1] |> List.map (fun y -> {x=width-1; y=y})  // Down the right side
    let topEdge = [0..width-2] |> List.map (fun x -> {x=x; y=0})           // Across the top (skip top-right)
    rightEdge @ topEdge

let rec CollectDiagonalsDownRight (start:coordinate) =
    seq {
        if start.x < width && start.y < height then    
            yield allLinesForward.[start.y].[start.x]
            yield! CollectDiagonalsDownRight {x=start.x+1; y=start.y+1}
    }

let rec CollectDiagonalsDownLeft (start:coordinate) =
    seq {
        if start.x >= 0 && start.y < height then      
            yield allLinesForward.[start.y].[start.x]
            yield! CollectDiagonalsDownLeft {x=start.x-1; y=start.y+1}
    }

// Convert coordinate sequences to strings
let diagonalsDownRight = 
    allTopLeftCoordinates 
    |> List.map (fun coord -> 
        CollectDiagonalsDownRight coord 
        |> Seq.toArray 
        |> String)
    |> List.filter (fun s -> s.Length > 0)

